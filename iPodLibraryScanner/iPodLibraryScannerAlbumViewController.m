- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    static NSString *cell_identifier = @"TrackCell";
    UITableViewCell *track_cell = [tableView dequeueReusableCellWithIdentifier:cell_identifier];
    if (track_cell == nil) {
        track_cell = [[UITableViewCell alloc] initWithStyle:UITableViewCellStyleSubtitle reuseIdentifier:cell_identifier];
    }
    
    MPMediaItem *song = [self.album.items objectAtIndex:indexPath.row];
    
    track_cell.textLabel.text = [song valueForProperty:MPMediaItemPropertyTitle];
    
    [tableView deselectRowAtIndexPath:indexPath animated:YES];
    
    return track_cell;
}

